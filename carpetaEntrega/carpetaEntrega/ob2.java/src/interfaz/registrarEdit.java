    private void objetoAPantalla(){
        tablemod.setRowCount(0);
        for (int i = 0; i < modelo.getListaEditoriales().size(); i++){
        String nombreEdit = modelo.getListaEditoriales().get(i).getNombre();
        String nombrePais = modelo.getListaEditoriales().get(i).getPaisDeOrigen();

       Object[] data = {nombreEdit, nombrePais};

       tablemod.addRow(data);

    }
         txtNombreEdit.setText("");
         txtNombrePais.setText("");
     }
