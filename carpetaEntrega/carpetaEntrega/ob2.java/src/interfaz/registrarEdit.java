    private void btnRegistrarEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarEditActionPerformed
        String nombreEdit = txtNombreEdit.getText();
        String nombrePais = txtNombrePais.getText();
        
        
          try {
    if (nombreEdit.isEmpty()  || nombrePais.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Deben estar completos todos los campos de texto antes de continuar.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
        if(modelo.editUnico(nombreEdit)){
            modelo.registrarEdit(nombreEdit, nombrePais);
            JOptionPane.showMessageDialog(this, "La editorial "+ nombreEdit + " ha sido registrada correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
             JOptionPane.showMessageDialog(this, "La editorial '" + nombreEdit + "' ya está en uso. Intente otro.", "Error", JOptionPane.ERROR_MESSAGE);
        }
} catch (Exception e) {
    // Manejar errores inesperados
    JOptionPane.showMessageDialog(this, "Ocurrió un error al registrar el genero, intente nuevamente", "Error", JOptionPane.ERROR_MESSAGE);
}
      
        
    }//GEN-LAST:event_btnRegistrarEditActionPerformed

